//Realizar un programa donde los numeros pares entre el 500 y el 1000

public class disminuyeAumeta {
    public static void main(String[] args)throws Exception {
        for(int i = 100; i >= 0; i -= 1){ //Se debe de dar un valor de iteracion
            System.out.println(i);
        }
        for(int i = 100; i <= 500; i += 2){ //Se debe de dar un valor de iteracion
            System.out.println(i);
        }
    }
}
